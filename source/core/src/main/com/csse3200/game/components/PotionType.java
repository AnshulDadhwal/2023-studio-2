package com.csse3200.game.components;

/**
 * Enumeration representing different types of potions within the game.
 * - `DEATH_POTION` represents a potion that has a death-related effect.
 * - `HEALTH_POTION` represents a potion that restores health.
 * - `SPEED_POTION` represents a potion that enhances the speed of characters.
 */
public enum PotionType {
    DEATH_POTION,
    HEALTH_POTION,
    SPEED_POTION,
}

